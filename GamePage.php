    <h1>WELCOME PLAY EVERYONE <span style="color:#30593D">Ark: Survival Evolved!</span></h1>
    <p>Ark: Survival Evolved (stylized as ΛRK) is a 2017 action-adventure survival video game developed by Studio Wildcard, in collaboration with Instinct Games, Efecto Studios, and Virtual Basement. In the game, players must survive being stranded on an island filled with roaming dinosaurs and other prehistoric animals, natural hazards, and potentially hostile human players.
The game is played from either a third-person or first-person perspective and its open world is navigated on foot or by riding a prehistoric animal. Players can use firearms and improvised weapons to defend against hostile humans and creatures, with the ability to build bases as defense on the ground and on some creatures. The game has both single-player and multiplayer options. Multiplayer allows the option to form tribes of members in a server. The max number of tribe mates varies from each server. In this mode all tamed dinosaurs and building structures are usually shared between the members. There is a PvE mode where players cannot fight each other.
Development began in October 2014, where it was first released on PC as an early access title in the middle of 2015. The development team conducted research into the physical appearance of the animals, but took creative license for gameplay purposes. Egypt-based developer Instinct Games was hired to facilitate the game's development. The game was released in August 2017 for PlayStation 4, Xbox One, Microsoft Windows, OS X and Linux, with versions for Android, iOS, and Nintendo Switch in 2018; a version for Stadia is scheduled for release in early 2021. It received generally mixed reviews, with criticism for its "punishing" difficulty, reliance on grinding, and performance issues, particularly on the Nintendo Switch version. Several expansions to the game have been released as downloadable content. The game begot two spin-off games in March 2018—virtual reality game Ark Park and sandbox survival game PixArk—and two companion apps: A-Calc in October 2015, and Dododex in August 2017.</p>
